package encapsulation;

public class SampleEncapsulation {
	
	private String mailId;
	private int accNo;
	private int accBalnc;
	public String getMailId() {
		return mailId;
	}
	public void setMailId(String mailId) {
		this.mailId = mailId;
	}
	public int getAccNo() {
		return accNo;
	}
	public void setAccNo(int accNo) {
		this.accNo = accNo;
	}
	public int getAccBalnc() {
		return accBalnc;
	}
	public void setAccBalnc(int accBalnc) {
		this.accBalnc = accBalnc;
	}
	
	

}
